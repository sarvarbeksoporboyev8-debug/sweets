/// Spacing constants for the Sweets app.
/// Provides consistent spacing values throughout the application.
class Spacing {
  Spacing._(); // Private constructor to prevent instantiation

  /// Extra small spacing: 4px
  static const double xs = 4.0;

  /// Small spacing: 8px
  static const double sm = 8.0;

  /// Medium spacing: 12px
  static const double md = 12.0;

  /// Large spacing: 16px
  static const double lg = 16.0;

  /// 14px spacing for specific button padding
  static const double buttonPadding = 14.0;

  /// Extra large spacing: 20px
  static const double xl = 20.0;

  /// 2x extra large spacing: 24px
  static const double xxl = 24.0;

  /// 3x extra large spacing: 32px
  static const double xxxl = 32.0;

  /// 4x extra large spacing: 40px
  static const double xxxxl = 40.0;

  /// 5x extra large spacing: 56px (button height)
  static const double buttonHeight = 56.0;

  /// Large gap spacing: 80px
  static const double gapLg = 80.0;
}
