/// Design system spacing constants for the Sweets app.
/// Based on an 8-point grid system for consistent spacing throughout the app.
class SweetsSpacing {
  // Base spacing scale (8-point grid system)
  static const double xs = 4.0;   // Extra small
  static const double sm = 8.0;   // Small
  static const double md = 12.0;  // Medium
  static const double lg = 16.0;  // Large
  static const double xl = 24.0;  // Extra large
  static const double xxl = 32.0; // 2X large
  static const double xxxl = 40.0; // 3X large
  static const double huge = 56.0; // Huge (for buttons, etc.)

  // Semantic spacing names
  static const double padding = lg;          // Default padding (16)
  static const double paddingSmall = md;     // Small padding (12)
  static const double paddingLarge = xl;     // Large padding (24)
  
  static const double margin = lg;           // Default margin (16)
  static const double marginSmall = md;      // Small margin (12)
  static const double marginLarge = xl;      // Large margin (24)
  
  static const double gutter = lg;           // Default gutter (16)
  static const double gap = md;              // Default gap between items (12)
  static const double gapSmall = sm;         // Small gap (8)
  static const double gapLarge = lg;         // Large gap (16)

  // Border radius
  static const double radiusSmall = 8.0;
  static const double radiusMedium = 12.0;
  static const double radiusLarge = 16.0;
  static const double radiusXLarge = 24.0;

  // Icon sizes
  static const double iconSmall = 20.0;
  static const double iconMedium = 24.0;
  static const double iconLarge = 32.0;

  // Common component heights
  static const double buttonHeight = 56.0;
  static const double inputHeight = 56.0;
  static const double navBarHeight = 56.0;
  static const double tabBarHeight = 88.0;
}
